package org.esperanto_france.samopiniuloj.modelo;

public class LudiGson {

    private String respondo;
    private String kialo;
    private int eraro_id;
    private Integer[] registritaj;
    private String[] kialoj;
    private Integer[] eraraj_proponoj;
    private String [] proponoj;
    private int vorto_id;
    private int ludanto_id;


    public String getRespondo() {
        return respondo;
    }

    public void setRespondo(String respondo) {
        this.respondo = respondo;
    }

    public String getKialo() {
        return kialo;
    }

    public void setKialo(String kialo) {
        this.kialo = kialo;
    }

    public int getEraro_id() {
        return eraro_id;
    }

    public void setEraro_id(int eraro_id) {
        this.eraro_id = eraro_id;
    }

    public Integer[] getRegistritaj() {
        return registritaj;
    }

    public void setRegistritaj(Integer[] registritaj) {
        this.registritaj = registritaj;
    }

    public String[] getKialoj() {
        return kialoj;
    }

    public void setKialoj(String[] kialoj) {
        this.kialoj = kialoj;
    }

    public Integer[] getEraraj_proponoj() {
        return eraraj_proponoj;
    }

    public void setEraraj_proponoj(Integer[] eraraj_proponoj) {
        this.eraraj_proponoj = eraraj_proponoj;
    }

    public String[] getProponoj() {
        return proponoj;
    }

    public void setProponoj(String[] proponoj) {
        this.proponoj = proponoj;
    }

    public int getVorto_id() {
        return vorto_id;
    }

    public void setVorto_id(int vorto_id) {
        this.vorto_id = vorto_id;
    }

    public int getLudanto_id() {
        return ludanto_id;
    }

    public void setLudanto_id(int ludanto_id) {
        this.ludanto_id = ludanto_id;
    }
}
