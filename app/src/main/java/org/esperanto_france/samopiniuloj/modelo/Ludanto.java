package org.esperanto_france.samopiniuloj.modelo;


public class Ludanto {

    private Integer id;

    private String kromnomo;
    private String retadreso;
    private String lando;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getKromnomo() {
        return kromnomo;
    }

    public void setKromnomo(String kromnomo) {
        this.kromnomo = kromnomo;
    }

    public String getRetadreso() {
        return retadreso;
    }

    public void setRetadreso(String retadreso) {
        this.retadreso = retadreso;
    }

    public String getLando() {
        return lando;
    }

    public void setLando(String lando) {
        this.lando = lando;
    }
}
